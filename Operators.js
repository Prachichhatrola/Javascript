// Arithmetic Operators
let a = 45;
let b = 4;
console.log("a + b = ", a+b)
console.log("a - b = ", a-b)
console.log("a / b = ", a/b)
console.log("a * b = ", a*b)
console.log("a ** b = ", a**b)
console.log("a % b = ", a%b)
console.log("a++ = ", a++)
console.log("a-- = ", a--)

// Assignment Operators
